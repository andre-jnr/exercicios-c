// Leia uma letra e imprima se é uma letra maiúscula, minúscula ou um caractere não alfabético.
#include <stdio.h>

int main()
{
  char caractere;

  printf("Digite um caractere: ");
  scanf(" %c", &caractere);

  switch (caractere)
  {
  case 'A':
  case 'B':
  case 'C':
  case 'D':
  case 'E':
  case 'F':
  case 'G':
  case 'H':
  case 'I':
  case 'J':
  case 'K':
  case 'L':
  case 'M':
  case 'N':
  case 'O':
  case 'P':
  case 'Q':
  case 'R':
  case 'S':
  case 'T':
  case 'U':
  case 'V':
  case 'W':
  case 'X':
  case 'Y':
  case 'Z':
    printf("Letra maiuscula\n");
    break;
  case 'a':
  case 'b':
  case 'c':
  case 'd':
  case 'e':
  case 'f':
  case 'g':
  case 'h':
  case 'i':
  case 'j':
  case 'k':
  case 'l':
  case 'm':
  case 'n':
  case 'o':
  case 'p':
  case 'q':
  case 'r':
  case 's':
  case 't':
  case 'u':
  case 'v':
  case 'w':
  case 'x':
  case 'y':
  case 'z':
    printf("Letra minuscula\n");
    break;
  default:
    printf("Caractere não alfabetico\n");
  }

  return 0;
}
